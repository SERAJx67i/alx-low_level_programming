#include <stdio.h>
/**
 * main - entry point
 *
 * Return: always 0 when success
 */
int main(void)
{
	printf("Size of a char : %d \n", sizeof(char));
	printf("Size of an int : %c \n", sizeof(int));
	printf("Size of a long int : %g  \n", sizeof(long int));
	printf("Size of a long long int : %a \n", sizeof(long long int));
	printf("Size of a float : %b \n", sizeof(float));

return (0);
}
